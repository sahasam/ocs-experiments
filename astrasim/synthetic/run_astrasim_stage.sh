#!/usr/bin/env bash
# Simulate a STAGE-generated workload through AstraSim (analytical backend).
#
# Uses STAGE's own validated reference configs (stage_configs/system.json with
# roofline-enabled + peak-perf, NO_MEMORY_EXPANSION) and a flat FullyConnected
# network sized to the rank count. The communicator-group file STAGE emits
# (comm_group.json) tells AstraSim which ranks participate in each collective,
# so the network topology stays flat -- that is exactly how STAGE was validated
# to 0.23% comm-volume error against a real 128-GPU H100 run.
#
# For the OCS study, override NETWORK_YML with a two-tier optical topology;
# the workload + comm-groups are unchanged (one workload, many network sweeps).
#
# Usage:
#   bash run_astrasim_stage.sh <workload_name> <npus>
#   bash run_astrasim_stage.sh llama3_8b_tp8_pp2_dp4 64
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$SCRIPT_DIR"

WORKLOAD_NAME=${1:?'workload name required (subdir under results/)'}
NPUS=${2:?'num npus required (= DP*TP*PP*SP)'}

RESULTS_DIR="results/${WORKLOAD_NAME}"
WORKLOAD_PREFIX="/app/llm-parallelism/astrasim/synthetic/${RESULTS_DIR}/${WORKLOAD_NAME}"
COMM_GROUP="/app/llm-parallelism/astrasim/synthetic/${RESULTS_DIR}/comm_group.json"

SYSTEM_CFG=${SYSTEM_CFG:-/app/llm-parallelism/astrasim/synthetic/stage_configs/system.json}
MEMORY_CFG=${MEMORY_CFG:-/app/llm-parallelism/astrasim/synthetic/stage_configs/memory.json}

# Network: default to a flat FullyConnected sized to NPUS (STAGE's reference).
# Override with NETWORK_YML=<path-in-container> for OCS / two-tier sweeps.
if [[ -z "${NETWORK_YML:-}" ]]; then
  NET_LOCAL="stage_configs/network_fc_${NPUS}.yml"
  cat > "$NET_LOCAL" <<EOF
# Auto-generated by run_astrasim_stage.sh -- flat FullyConnected, ${NPUS} NPUs.
topology: [ FullyConnected ]
npus_count: [ ${NPUS} ]
bandwidth: [ 50.0 ]   # GB/s per link per direction
latency: [ 500.0 ]    # ns
EOF
  NETWORK_YML="/app/llm-parallelism/astrasim/synthetic/${NET_LOCAL}"
  echo "Using flat network: $NET_LOCAL"
fi

# STAGE's reference uses the Congestion_Unaware analytical binary.
ASTRASIM_BIN=${ASTRASIM_BIN:-/app/astra-sim/build/astra_analytical/build/bin/AstraSim_Analytical_Congestion_Unaware}
# astra-sim-bigmem = astra-sim:latest + jemalloc. jemalloc's arena strategy
# (plus the raised Docker VM memory budget) avoids the glibc heap corruption
# the stock image hits when many heavy ranks run in one process.
ASTRASIM_IMAGE=${ASTRASIM_IMAGE:-astra-sim-bigmem:latest}
DOCKER_SHM=${DOCKER_SHM:-4g}
LOG_DIR="${RESULTS_DIR}/logs"
mkdir -p "$LOG_DIR"
LOG="${LOG_DIR}/astrasim_stage.log"

# Debug-trace folder: with trace-enabled:1 in the system config, AstraSim's
# Workload emits per-node issue/callback events to <logging-folder>/log.log at
# debug level. Point it at a host-mounted path so the trace is readable. Set
# TRACE_FOLDER='' to skip the flag (default container 'log' dir, not host-visible).
TRACE_SUBDIR=${TRACE_SUBDIR:-trace}
LOG_FOLDER_CONTAINER="/app/llm-parallelism/astrasim/synthetic/${LOG_DIR}/${TRACE_SUBDIR}"
mkdir -p "${LOG_DIR}/${TRACE_SUBDIR}"

echo "Running AstraSim on $WORKLOAD_NAME ($NPUS ranks) [image=$ASTRASIM_IMAGE] ..."
docker run --rm --shm-size="$DOCKER_SHM" \
  -v /Users/sahas/workplace/astra-sim:/app/astra-sim \
  -v /Users/sahas/workplace/llm-parallelism:/app/llm-parallelism \
  "$ASTRASIM_IMAGE" \
  "$ASTRASIM_BIN" \
    --workload-configuration="$WORKLOAD_PREFIX" \
    --comm-group-configuration="$COMM_GROUP" \
    --system-configuration="$SYSTEM_CFG" \
    --network-configuration="$NETWORK_YML" \
    --remote-memory-configuration="$MEMORY_CFG" \
    --logging-folder="$LOG_FOLDER_CONTAINER" \
  > "$LOG" 2>&1

echo "--- AstraSim per-rank summary (sys[0]) ---"
grep -E "sys\[0\], (Wall time|GPU time|Comm time)|sys\[0\] finished" "$LOG" | tail -4
echo "exit signal: $(tail -1 "$LOG")"
echo "Full log: $LOG"
